#operator aritmatika

nilai1 = 7
nilai2 = 3
#penjumlahan
print(nilai1 + nilai2)
#pengurangan
print(nilai1 - nilai2)
#perkalian
print(nilai1 * nilai2)
#pembagian
print(nilai1 / nilai2)
#sisa bagi
print(nilai1 % nilai2)
#pangkat
print(nilai1 ** nilai2)
#pembagian bulat
print(nilai1 // nilai2)


#operator penugasan

#sama dengan
nilai = 3
print(nilai)
#tambah sama dengan
nilai += 5
print(nilai)
#kurang sama dengan
nilai -= 4
print(nilai)
#kali sama dengan
nilai *= 8
print(nilai)
#bagi sama dengan
nilai /= 9
print(nilai)
#sisa bagi sama dengan
nilai %= 2
print(nilai)
#pangkat sama dengan
nilai **= 6
print(nilai)
#pembagian bulat sama dengan
nilai //= 7
print(nilai)


#operator perbandingan

#sama dengan
print(3 == 7)
#tidak sama dengan
print(3 != 7)
#lebih besar dari
print(3 > 7)
#lebih kecil
print(3 < 7)
#lebih besar sama dengan
print(3 >= 7)
#lebih kecil sama dengan
print(3 <= 7)


